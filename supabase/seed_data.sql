-- ----------------------------------------------------------------
-- §4  rating · review_count 갱신
-- ----------------------------------------------------------------

UPDATE treatments t
SET
  review_count = sub.cnt,
  rating = ROUND(sub.avg_r::numeric, 1)
FROM (
  SELECT item_id,
         COUNT(*)::int               AS cnt,
         AVG(rating)                 AS avg_r
  FROM reviews
  WHERE item_type = 'treatment'
  GROUP BY item_id
) sub
WHERE t.id = sub.item_id;

UPDATE devices d
SET
  review_count = sub.cnt,
  rating = ROUND(sub.avg_r::numeric, 1)
FROM (
  SELECT item_id,
         COUNT(*)::int               AS cnt,
         AVG(rating)                 AS avg_r
  FROM reviews
  WHERE item_type = 'device'
  GROUP BY item_id
) sub
WHERE d.id = sub.item_id;
