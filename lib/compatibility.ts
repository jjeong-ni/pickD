function clamp(v: number) { return Math.max(30, Math.min(98, v)); }

export function calcCompatibility(
  category: string,
  skinType: string,
  faceShape: string | null,
  itemType: 'device' | 'treatment',
): CompatResult {
  const skinMap = itemType === 'device' ? DEV_SKIN : TRT_SKIN;
  const faceMap = itemType === 'device' ? DEV_FACE : TRT_FACE;
  const skinR   = itemType === 'device' ? DEV_SKIN_R : TRT_SKIN_R;
  const faceR   = itemType === 'device' ? DEV_FACE_R : TRT_FACE_R;

  const skinScore = clamp(skinMap[category]?.[skinType] ?? 75);
  const faceDelta = faceShape ? (faceMap[category]?.[faceShape] ?? 0) : 0;
  const faceScore = clamp(78 + faceDelta);
  const score = Math.round(skinScore * 0.65 + faceScore * 0.35);

  const rt = skinR[category];
  const skinReason = rt
    ? (skinScore >= 80 ? rt.good : skinScore >= 60 ? rt.ok : rt.bad)
    : `${skinType} 피부 타입 기반으로 분석한 적합도에요`;

  const noun = itemType === 'device' ? '기기' : '시술';
  const specific = faceShape ? faceR[category]?.[faceShape] : undefined;
  const faceReason = specific
    ? specific
    : faceDelta >= 5
    ? `${faceShape ?? ''}형 얼굴에 이 ${noun}가 특히 잘 어울려요`
    : faceDelta <= -3
    ? `${faceShape ?? ''}형 얼굴에는 다른 ${noun}가 더 효과적일 수 있어요`
    : `${faceShape ?? ''}형 얼굴에 두루 무난하게 사용할 수 있어요`;

  return { score, skinScore, faceScore, skinReason, faceReason };
}
